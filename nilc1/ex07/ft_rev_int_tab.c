void	ft_rev_int_tab(int *tab, int size)
{
	int	n;
	int	m;
	int	rev;

	n = 0;
	m = size - 1;
	while (n < size / 2)
	{
		rev = tab[n];
		tab[n] = tab[m];
		tab[m] = rev;
		n++;
		m--;
	}
}
